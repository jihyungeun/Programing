public class Ex_07 {
	public static void main(String args[]){
		int a = 10;
		System.out.println("a = " + a);
		a += 4;
		System.out.println("a += 4의 결과 " + a);
		a -= 4;
		System.out.println("a -= 4의 결과 " + a);
		a *= 4;
		System.out.println("a *= 4의 결과 " + a);
		a /= 4;
		System.out.println("a /= 4의 결과 " + a);
		a %= 4;
		System.out.println("a %= 4의 결과 " + a);
	}
}